package secondpackage;

public class A27Increment {
    public static void main(String[] args) {
        int x = 5;
        int y = x++;
        System.out.println(" " + y + " " + x);

        int a = 5;
        int b = ++a;
        System.out.println(" " + b + " " + a);

        int w = 5;
        w++;
        System.out.println(w);

        int z = 5;
        ++z;
        System.out.println(w);
    }
}
