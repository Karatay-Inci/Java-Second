package FEB9;

public class B14DoWhile {
    public static void main(String[] args) {
        int counter = 10;
        do{
            System.out.println("Counter : "+ counter);
            counter ++;
        }
        while (counter<10);
        System.out.println("With while loop");

        int counterx=10;
        while (counterx<=10){
            System.out.println(counterx);
            counterx++;
        }
    }
}
