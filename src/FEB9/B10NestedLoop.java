package FEB9;

public class B10NestedLoop {
    public static void main(String[] args) {
        for(int i=0;    i<5;    i++){
            for(int j=0;    j<6;    j++){
                //System.out.print("Hello World ");
                System.out.println(" i : " +i+ "j : " +j );
            }
        }
    }
}
